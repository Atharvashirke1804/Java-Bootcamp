 class ElseifDemo7 {

    public static void main(String[] args) {

        int number = -45;

        if (number > 0) {
            System.out.println("Positive number.");

        } else if (number < 0) {
            System.out.println("Negative number.");

        } else {
            System.out.println("Zero.");
        }
    }
}
