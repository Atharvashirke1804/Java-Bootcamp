 // Write a program to whether a number is multiple of 5 or not
 
 class Que4 {

    public static void main(String[] args) {

        int num = 55;
    
        if (num % 5 == 0) {

            System.out.println(num + " is a multiple of 5.");
        } else {
            System.out.println(num + " is not a multiple of 5.");
        }
    }
}
