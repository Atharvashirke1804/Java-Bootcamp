public class Comparision3 {
    
    public static void main (String[] args){

        byte a = 44;
        float b = 45.4f;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);


    } 

}
