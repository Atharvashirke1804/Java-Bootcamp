public class Comparision4 {

    public static void main (String[] args){

        long a = 5435;
        char b = '7';

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);

    }

}
