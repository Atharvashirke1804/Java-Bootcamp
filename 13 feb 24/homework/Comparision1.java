 class Comparision1 {
    
    public static void main (String[] args){

        long a = 543245;
        char b = '6';
        short c = 121;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= c);
        System.out.println(a <= b);
        System.out.println(a == c);
        System.out.println(a != b);

    }

}
