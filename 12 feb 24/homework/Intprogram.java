class Intprogram{

    public static void main(String[] args){

        int a = 78;
        int b = 865;

        System.out.println("The value in byte:  "+(byte) (a+b));
        System.out.println("The value in long:  "+(long) (a-b) );
        System.out.println("The value in short:  "+(short) (a*b));
        System.out.println("The value in float:  "+(float) (a%b));
        System.out.println("The value in double:  "+(double) (a*b-a));
        System.out.println("The value in char:  "+(char)a); 
     

    }
}