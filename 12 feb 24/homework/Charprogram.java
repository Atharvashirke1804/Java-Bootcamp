class Charprogram{

    public static void main (String[] args){

        char a = '9';
        char b = '4';

        System.out.println("The value in int is  "+   (int)(a +b));
        System.out.println("The value in byte is "+  (byte)(a-b));
        System.out.println("The value in short is "+ (short)(a*b));
        System.out.println("The value in long is "+  (long)(a/b));
        System.out.println("The value in float is "+ (float)(a%b));
        System.out.println("The value in double is "+(double)(a*b));

    }
}
